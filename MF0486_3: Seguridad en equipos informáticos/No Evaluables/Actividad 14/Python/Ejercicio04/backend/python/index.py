# This Python code snippet is asking the user to input two numbers, converting them to floating-point
# numbers, and then performing basic arithmetic operations on them.

num1 = float(input("Dime el primer número: "));
num2 = float(input("Dime el segundo número: "));
print("La suma es:", num1 + num2);
print("La resta es:", num1 - num2);
print("La multiplicación es:", num1 * num2);
print("La división es:", num1 / num2);